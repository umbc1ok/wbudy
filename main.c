void beep(tU16 repetitions)
{

	tU32 it = 0;
	tU32 timesPlayed = 0;
	PINSEL1 &= ~0x000C0000;
	PINSEL1 |= 0x00080000;

	while (repetitions != timesPlayed)
	{
		if (it == 8600)
		{
			it = 0;
			sdelay(1);
			timesPlayed++;
		}
		tU8 buffer = myArray[it];
		DACR = (buffer << 8) | (1 << 16);
		it++;
		udelay(90);
	}
	PINSEL1 &= ~(0x3 << 18);
}

