void beep(tU16 repetitions){

	tU32 it = 0;
	// to powinno grac sinusoidalny dzwiek (czy cos w ten desen) w nieskonczonosc
	PINSEL1 &= ~0x000C0000;
	PINSEL1 |=  0x00080000;
	tU32 timesPlayed = 0;
	while(repetitions!=timesPlayed){
		if(it == (tU32)8659){
			it = 0;
			sdelay(1);
			timesPlayed++;
		}
		tU8 buffer = samples[it];
		DACR = (buffer << (tU8)8) | ((tU8)1 <<(tU8)16);        //BIAS = 0, 1uS settling time
		it++;
		udelay(90);			// this function might not work yet (idk)
	}
	//wylaczenie glosnika
	PINSEL1 &= ~(0x3 << 18);
}
